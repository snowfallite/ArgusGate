W0       = 2268   # метка (Тест кейс ID / Заголовок / …)
W1       = 1134   # № шага
W2       = 1701   # Шаг
W3       = 2268   # Ожидаемый результат
W4       = 1134   # Статус
W5       = 567    # Коммент.
W_MERGED = W1 + W2 + W3 + W4 + W5  # 6804 twips — cols 1-5 merged

EN = '–'  # en-dash

# ─────────────────────────────────────────────────────────────────────────────
# XML-хелперы
# ─────────────────────────────────────────────────────────────────────────────

def _set_font(run, bold=False):
    run.bold = bold
    run.font.size = SZ
    run.font.name = FONT
    rPr = run._r.get_or_add_rPr()
    rFonts = rPr.find(qn('w:rFonts'))
    if rFonts is None:
        rFonts = OxmlElement('w:rFonts')
        rPr.insert(0, rFonts)
    for attr in ('w:ascii', 'w:hAnsi', 'w:cs', 'w:eastAsia'):
        rFonts.set(qn(attr), FONT)


def _para_spacing(para, before=40, after=40, line=240):
    """Межстрочный интервал и отступы — точно как в оригинале."""
    pf = para.paragraph_format
    pf.alignment = WD_ALIGN_PARAGRAPH.LEFT
    pf.first_line_indent = Pt(0)
    pPr = para._p.get_or_add_pPr()
    spacing = pPr.find(qn('w:spacing'))
    if spacing is None:
        spacing = OxmlElement('w:spacing')
        pPr.append(spacing)
    spacing.set(qn('w:before'),   str(before))
    spacing.set(qn('w:after'),    str(after))
    spacing.set(qn('w:line'),     str(line))
    spacing.set(qn('w:lineRule'), 'auto')


def _set_cell_width(cell, width_twips):
    tcPr = cell._tc.get_or_add_tcPr()
    tcW = tcPr.find(qn('w:tcW'))
    if tcW is None:
        tcW = OxmlElement('w:tcW')
        tcPr.insert(0, tcW)
    tcW.set(qn('w:w'),    str(width_twips))
    tcW.set(qn('w:type'), 'dxa')


def _cell_write(cell, text, bold=False):
    para = cell.paragraphs[0]
    _para_spacing(para)
    run = para.add_run(text)
    _set_font(run, bold=bold)


def _add_table_cell_margins(table, top=55, left=108, bottom=55, right=108):
    """Добавляет tblCellMar к таблице — отступы внутри каждой ячейки."""
    tblPr = table._tbl.find(qn('w:tblPr'))
    if tblPr is None:
        tblPr = OxmlElement('w:tblPr')
        table._tbl.insert(0, tblPr)
    tblCellMar = tblPr.find(qn('w:tblCellMar'))
    if tblCellMar is not None:
        tblPr.remove(tblCellMar)
    tblCellMar = OxmlElement('w:tblCellMar')
    for side, val in (('w:top', top), ('w:left', left),
                      ('w:bottom', bottom), ('w:right', right)):
        el = OxmlElement(side)
        el.set(qn('w:w'),    str(val))
        el.set(qn('w:type'), 'dxa')
        tblCellMar.append(el)
    tblPr.append(tblCellMar)


def _set_table_width(table, width_twips=9072):
    tblPr = table._tbl.find(qn('w:tblPr'))
    if tblPr is None:
        tblPr = OxmlElement('w:tblPr')
        table._tbl.insert(0, tblPr)
    tblW = tblPr.find(qn('w:tblW'))
    if tblW is None:
        tblW = OxmlElement('w:tblW')
        tblPr.insert(0, tblW)
    tblW.set(qn('w:w'),    str(width_twips))
    tblW.set(qn('w:type'), 'dxa')


def _add_borders(table):
    """Тонкие чёрные рамки — как в оригинале."""
    tblPr = table._tbl.find(qn('w:tblPr'))
    if tblPr is None:
        tblPr = OxmlElement('w:tblPr')
        table._tbl.insert(0, tblPr)
    bdr = tblPr.find(qn('w:tblBorders'))
    if bdr is not None:
        tblPr.remove(bdr)
    bdr = OxmlElement('w:tblBorders')
    for side in ('w:top', 'w:left', 'w:bottom', 'w:right',
                 'w:insideH', 'w:insideV'):
        el = OxmlElement(side)
        el.set(qn('w:val'),   'single')
        el.set(qn('w:sz'),    '4')
        el.set(qn('w:space'), '0')
        el.set(qn('w:color'), '000000')
        bdr.append(el)
    tblPr.append(bdr)


# ─────────────────────────────────────────────────────────────────────────────
# Построение одного тест-кейса
# ─────────────────────────────────────────────────────────────────────────────

def _merge_header_row(table, row_idx, label, value, bold_val=False):
    """Строка: [label col0] [value — merged cols 1-5]."""
    row = table.rows[row_idx]
    c0      = row.cells[0]
    merged  = row.cells[1].merge(row.cells[5])
    _set_cell_width(c0,     W0)
    _set_cell_width(merged, W_MERGED)
    _cell_write(c0,     label, bold=True)
    _cell_write(merged, value, bold=bold_val)


def add_test_case(doc, tc_id, name, preconditions, steps, postconditions=''):
    """Добавляет заголовок + таблицу одного тест-кейса."""

    # ── Заголовок таблицы ────────────────────────────────────────────────────
    cap = doc.add_paragraph()
    cap.alignment = WD_ALIGN_PARAGRAPH.LEFT
    pPr = cap._p.get_or_add_pPr()
    sp = OxmlElement('w:spacing')
    sp.set(qn('w:before'),   '240')
    sp.set(qn('w:after'),    '120')
    sp.set(qn('w:line'),     '240')
    sp.set(qn('w:lineRule'), 'auto')
    pPr.append(sp)
    ind = OxmlElement('w:ind')
    ind.set(qn('w:firstLine'), '0')
    pPr.append(ind)
    run = cap.add_run(f'Таблица Б.{tc_id} {EN} Тест кейс {tc_id}')
    _set_font(run, bold=False)

    # ── Таблица ───────────────────────────────────────────────────────────────
    n_rows = 3 + 1 + len(steps) + 1   # Заголовок×3 + шапка шагов + шаги + Постусловие
    table  = doc.add_table(rows=n_rows, cols=6)
    table.style = 'Table Grid'

    _set_table_width(table, 9072)
    _add_borders(table)
    _add_table_cell_margins(table, top=55, left=108, bottom=55, right=108)

    # Row 0 — Тест кейс ID
    _merge_header_row(table, 0, 'Тест кейс ID', str(tc_id), bold_val=True)

    # Row 1 — Заголовок
    _merge_header_row(table, 1, 'Заголовок', name, bold_val=False)

    # Row 2 — Предусловие
    _merge_header_row(table, 2, 'Предусловие', preconditions, bold_val=False)

    # Row 3 — шапка шагов
    col_headers = [' ', '№ шага', 'Шаг', 'Ожидаемый результат', 'Статус', 'Коммент.']
    widths      = [W0,   W1,       W2,    W3,                     W4,       W5]
    row3 = table.rows[3]
    for ci, (hdr, w) in enumerate(zip(col_headers, widths)):
        c = row3.cells[ci]
        _set_cell_width(c, w)
        _cell_write(c, hdr, bold=True)

    # Строки шагов
    for si, (step_text, expected) in enumerate(steps):
        row = table.rows[4 + si]
        vals = [' ', str(si + 1), step_text, expected, 'Пройден', ' ']
        for ci, (v, w) in enumerate(zip(vals, widths)):
            c = row.cells[ci]
            _set_cell_width(c, w)
            _cell_write(c, v, bold=False)

    # Последняя строка — Постусловие
    _merge_header_row(table, n_rows - 1, 'Постусловие',
                      postconditions, bold_val=False)


